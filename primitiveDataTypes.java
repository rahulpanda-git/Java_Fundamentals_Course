public class primitiveDataTypes {
    public static void main (String[] args){
        //Boolean Data Type - represents TRUE or FALSE
        boolean status = false;
        System.out.println("The value of status boolean variable is "+status);
        status = true;
        System.out.println("Now the value of status boolean variable is "+status);

        //Int data type represents whole numbers
        int number1 = 1;
        System.out.println("The value of integer number1 is "+number1);

        //Double data type represents a decimal number
        double pi = 3.14;
        System.out.println("The value of pi is "+pi);

        //Char data type represents a single character or symbol
        char approval='Y';
        System.out.println("The status of Approval is: "+approval);
    }
}